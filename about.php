										  <h2>Responsible Conduct of Research Training</h2>
										  <p>Sidebar on the left, content on the right.</p>
										</header>
										
										<span class="image featured"><img src="images/banner.jpg" alt="" /></span>
										<p>As of January 4, 2010, NSF requires that all undergraduate students, graduate students, and postdoctoral researchers supported by NSF to conduct research receive training in the <a href="http://www.nyu.edu/research/resources-and-support-offices/training-and-certification/rcr.html#quadBox_nyurichtext">Responsible Conduct of Research (RCR)</a>.&nbsp; Additionally, NIH National Research Service Award recipients (paid from both training grants and individual fellowships) and K awardees are also mandated to train in RCR.&nbsp; If your department has forwarded to us your name, you are a researcher who is required to complete RCR training this year.</p>
<p>The Office of Postdoctoral Affairs at NYU is organizing two sessions per semester of RCR training for the whole Washington Square Campus to discuss the topics of publication &amp; authorship; and research misconduct &amp; conflict of interest.&nbsp; These sessions are intended to help researchers learn survival skills &amp; ethics to build and advance their careers.&nbsp; </p>
<p>To supplement these broad RCR training sessions, each department will be offering its researchers more focused, department-specific RCR training sessions.&nbsp; Your department will contact you directly to notify you of their supplemental RCR program.&nbsp; </p>
<p>In order to fulfill your RCR requirement, you must attend two of the four sessions offered by the Office of the Senior Vice Provost for Research this academic year, as well your department’s supplemental RCR sessions.&nbsp; If you are an undergraduate, you may bypass these trainings and work directly with your mentor to fulfill your requirement under the guidance of the Office of Postdoctoral Affairs.&nbsp; If you attended RCR sessions last academic year but did not fulfill your requirement in May, then the sessions you attended will be counted toward this year’s requirement.&nbsp; </p>
                                        <p>Beginning January 2011, NYU Washington Square has developed a series of sessions on Responsible Conduct of Research which is open to the entire University community.&nbsp; This instruction incorporates many of the elements of the previously-offered Workshop for Emerging Scientists, which was designed to address NIH requirements for training grants, but also meets the mandates of the newly-required National Science Foundation Competes Act.&nbsp; All science departments, including associated schools such as the Courant Institute of Mathematical Sciences, the College of Dentistry, and the Steinhardt School of Culture, Education, and Human Development, which receive funding from NIH or NSF, have trainees who are required to participate in this program.</p>
<p>University wide training sessions in RCR are directed by Dr. Christine Ponder and the Office of Postdoctoral Affairs. The curriculum was developed in coordination with Marti L. Dunne, Associate Vice Provost for Research Compliance and Administration, Dr. Keith Micoli, NYU School of Medicine Ethics Program Coordinator and a faculty committee consisting of senior researchers from the affected schools and departments.  </p>
<p>Participating departments include the Courant Institute, Steinhardt, Anthropology, Economics, Linguistics, Politics, Physics, The College of Nursing, and the College of Dentistry. Certification by the university-wide program RCR program includes attendance at the two “Bookend” sessions offered each semester, along with varying department specific requirements. Postdocs and Graduate Students in the departments of Biology, Psychology, and the Center for Neural Science fulfill the RCR requirement with the Emerging Scientist Workshop. Postdocs and Graduate Students in Chemistry fulfill the RCR requirement with the Departmental Professional Development Course. Any continuing trainees who have been in University programs more than 4 years are required to repeat the training, as are faculty and research career candidates receiving new awards in specifically mandated categories; this refresher can be satisfied by participating in the curriculum as a discussion group leader or teacher/facilitator.<br>
</p>
<p>Compliance is monitored by the Office of Postdoctoral Affairs via on-line registration by participants and actual attendance records. Materials used may include such elements as case studies, on-line and print materials available from NAS and ORI, and the textbook Scientific Integrity, 3rd Ed. by F.L. Macrina (ASM press, 2005).</p>
<p>The Office of Postdoctoral Affairs at NYU organizes two sessions per semester of RCR training for the whole Washington Square Campus to discuss the topics of publication &amp; authorship and research misconduct &amp; conflict of interest.&nbsp; These sessions, which include all of the elements mandated by the Federal Government, are intended to help researchers learn survival skills &amp; ethics to build and advance their careers.&nbsp; To supplement these broad RCR training sessions, each department offers its researchers more focused, department-specific RCR training sessions or online training through CITI.&nbsp; For Federally mandated researchers to fulfill their RCR requirement, they must attend two of the four annual sessions offered by the Office of Postdoctoral Affairs as well their department’s supplemental RCR sessions.&nbsp; Undergraduates may bypass these trainings and work directly with their mentors to fulfill their requirement.&nbsp;</p>
<p>For specific information regarding the RCR training sessions offered by the Office of Postdoctoral Affairs,&nbsp;<a href="http://www.nyu.edu/content/nyu/en/research/resources-and-support-offices/training-and-certification/rcrtraining.html#quadBox_nyurichtext">click here</a>.&nbsp; For more information on departmental supplemental RCR training programs, please contact the individual department directly.</p>
										
						
